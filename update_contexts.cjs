let updatedJSON = 0;
try {
    let jsonStr = fs.readFileSync(jsonFilePath, 'utf8');
    const data = JSON.parse(jsonStr);
    for (const item of data) {
        if (updates[item.id]) {
            item.prompt_text = updates[item.id].prompt_text;
            item.hidden_agenda = updates[item.id].hidden_agenda;
            updatedJSON++;
        }
    }
    fs.writeFileSync(jsonFilePath, JSON.stringify(data, null, 2), 'utf8');
    console.log('JSON updated:', updatedJSON);
} catch (e) {
    console.error('JSON err', e);
}

let updatedTS = 0;
try {
    let tsStr = fs.readFileSync(tsFilePath, 'utf8');
    for (const id in updates) {
        const idRegex = new RegExp(`"id":\\s*"${id}"[\\s\\S]*?\\}`);
        const match = tsStr.match(idRegex);
        if (match) {
            let block = match[0];
            block = block.replace(/"prompt_text":\s*"[^"]*"/, `"prompt_text": "${updates[id].prompt_text}"`);
            block = block.replace(/"hidden_agenda":\s*"[^"]*"/, `"hidden_agenda": "${updates[id].hidden_agenda}"`);
            tsStr = tsStr.replace(match[0], block);
            updatedTS++;
        }
    }
    fs.writeFileSync(tsFilePath, tsStr, 'utf8');
    console.log('TS updated:', updatedTS);
} catch (e) {
    console.error('TS err', e);
}
