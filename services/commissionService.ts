/**
 * Сгенерировать промпт для совещательной комиссии
 */
export function buildAdvisoryCommissionPrompt(
  dialogSummary: string,
  activeMembers: { member: AdvisoryCommissionMember; triggeredBy: string[] }[],
  scenarioName: string
): string {
  if (activeMembers.length === 0) {
    return '';
  }

  const membersPrompts = activeMembers.map(({ member, triggeredBy }, i) => 
    `ПЕРСОНАЖ ${i + 1}: ${member.name}\n` +
    `ТИТУЛ: ${member.title}\n` +
    `ВОЗРАСТ: ${member.age || '???'}\n` +
    `СРАБОТАВШИЕ ТРИГГЕРЫ: ${triggeredBy.join(', ')}\n\n` +
    `${member.prompt}`
  ).join('\n\n---\n\n');

  return `
[СИСТЕМА: СОВЕЩАТЕЛЬНАЯ КОМИССИЯ — ГОЛОСА ОБЩЕСТВА]

Ты — генератор реплик для совещательной комиссии.
Это НЕ профессионалы, а гротескные персонажи, представляющие разные слои общества.
Их оценки НЕ влияют на итоговый балл, но показывают, с какими мнениями учителю предстоит столкнуться в реальности.

═══════════════════════════════════════════════════════════════════════════════
ИСХОДНАЯ СИТУАЦИЯ (ИНЦИДЕНТ):
═══════════════════════════════════════════════════════════════════════════════

${scenarioName}

═══════════════════════════════════════════════════════════════════════════════
АКТИВНЫЕ УЧАСТНИКИ (сработали триггеры):
═══════════════════════════════════════════════════════════════════════════════

${membersPrompts}

═══════════════════════════════════════════════════════════════════════════════
МАТЕРИАЛЫ:
═══════════════════════════════════════════════════════════════════════════════

СТЕНОГРАММА:
${dialogSummary}

═══════════════════════════════════════════════════════════════════════════════
ФОРМАТ ОТВЕТА (JSON):
═══════════════════════════════════════════════════════════════════════════════

{
  "advisory": [
    {
      "id": "<id персонажа>",
      "name": "<имя персонажа>",
      "title": "<титул>",
      "score": <оценка от 0 до 10 в стиле персонажа>,
      "verdict": "<вердикт В ХАРАКТЕРНОМ СТИЛЕ персонажа>",
      "triggered_by": ["<триггер1>", "<триггер2>"]
    }
  ]
}

⚠️ КРИТИЧЕСКИ ВАЖНО ДЛЯ JSON:
- Убедись, что все строки экранированы.
- Если в тексте вердикта есть кавычки, экранируй их: \"
- Не используй переносы строк внутри значений JSON, используй \n
- Ответ должен быть валидным JSON, который парсится JSON.parse()

⚠️ ЯЗЫКОВОЙ ПРОТОКОЛ: ВСЕ ВЕРДИКТЫ СТРОГО НА РУССКОМ ЯЗЫКЕ. НИКАКОГО АНГЛИЙСКОГО.

ВАЖНО:
- Каждый персонаж говорит СТРОГО в своём стиле (см. промпт)
- Оценки субъективны и отражают ПРЕДВЗЯТОСТЬ персонажа
- Вердикты должны быть яркими, характерными, гротескными
- Это сатира на общественные мнения — не бойся преувеличивать
`;
}

