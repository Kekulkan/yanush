/**
 * Проверить, содержит ли текст триггеры члена комиссии
 */
export function checkTriggers(text: string, member: AdvisoryCommissionMember): string[] {
  const lowerText = text.toLowerCase();
  const triggeredBy: string[] = [];
  
  for (const trigger of member.triggers) {
    if (lowerText.includes(trigger.toLowerCase())) {
      triggeredBy.push(trigger);
    }
  }
  
  return triggeredBy;
}

/**
 * Извлечь весь текст из сообщений для анализа триггеров
 */
export function extractAllText(messages: Message[]): string {
  return messages
    .map(m => {
      let text = m.content;
      if (m.state?.thought) text += ' ' + m.state.thought;
      if (m.state?.world_event?.description) text += ' ' + m.state.world_event.description;
      return text;
    })
    .join(' ');
}

/**
 * Определить, какие члены совещательной комиссии должны проснуться
 */
export function getActiveAdvisoryMembers(messages: Message[]): {
  member: AdvisoryCommissionMember;
  triggeredBy: string[];
}[] {
  const allText = extractAllText(messages);
  const activeMembers: { member: AdvisoryCommissionMember; triggeredBy: string[] }[] = [];
  
  for (const member of ADVISORY_COMMISSION) {
    // TuMOX@ (Тимофей Мохов) всегда активен
    if (member.alwaysActive) {
      activeMembers.push({ member, triggeredBy: ['ВСЕГДА АКТИВЕН'] });
      continue;
    }
    
    // Правдоруб — случайный шанс
    if (member.randomChance && Math.random() < member.randomChance) {
      activeMembers.push({ member, triggeredBy: ['СЛУЧАЙНОЕ ПОЯВЛЕНИЕ'] });
      continue;
    }
    
    // Проверка триггеров
    const triggeredBy = checkTriggers(allText, member);
    if (triggeredBy.length > 0) {
      activeMembers.push({ member, triggeredBy });
    }
  }
  
  return activeMembers;
}

